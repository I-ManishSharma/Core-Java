class P27 {
	public static void square(int a, int b, int c) {

		System.out.println("Square of " + a + " is " + (a * a));
		System.out.println("Square of " + b + " is " + (b * b));
		System.out.println("Square of " + c + " is " + (c * c));
	}

	public static void main(String[] args) {
		square(4, 7, 9);
	}
}