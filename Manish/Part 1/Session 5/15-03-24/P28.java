class P28 {
	public static void cube(int a, int b, int c) {

		System.out.println("Square of " + a + " is " + (a * a * a));
		System.out.println("Square of " + b + " is " + (b * b * b));
		System.out.println("Square of " + c + " is " + (c * c * c));
	}

	public static void main(String[] args) {
		cube(5, 2, 15);
	}
}